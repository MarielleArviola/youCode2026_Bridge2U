/* ---------- FONT SIZE ---------- */
let fontScale = localStorage.getItem("fontScale")
  ? parseFloat(localStorage.getItem("fontScale"))
  : 1;

function applyFontScale() {
  document.documentElement.style.fontSize = `${fontScale}rem`;
  localStorage.setItem("fontScale", fontScale);
}

if (fontIncreaseBtn) {
  fontIncreaseBtn.addEventListener("click", () => {
    fontScale += 0.1;
    applyFontScale();
  });
}

if (fontDecreaseBtn) {
  fontDecreaseBtn.addEventListener("click", () => {
    if (fontScale > 0.8) {
      fontScale -= 0.1;
      applyFontScale();
    }
  });
}

if (fontResetBtn) {
  fontResetBtn.addEventListener("click", () => {
    fontScale = 1;
    applyFontScale();
  });
}

